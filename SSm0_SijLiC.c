#include "SSm0_internal.h"

double tldS0liMR(double x, double y) {

  double sqrOneMy2 = sqrt(1-y*y);

  /* polynomials from Li arguments */
  double p1 = 1 - x;
  double p3 = 1 + x;
  double p4 = x - y;
  double p5 = 1 - y;
  double p7 = 1 + y;
  double p8 = 1 + x*x - 2*x*y;
  double p9 = 1 - x*y;
  double p10 = sqrOneMy2;
  double p11 = 1 - x*x - 2*x*y + 2*x*x*y*y;

  double inv2 = 1/((double)2);

  double PI = M_PI;

  double  wSliMR[76];

  wSliMR[1] = log(x);
  wSliMR[2] = atan2( - p10,p4);
  wSliMR[3] = rat(5,6);
  wSliMR[4] = rat(10,3);
  wSliMR[5] = atan2(p10,y);
  wSliMR[6] = rat(-10,3);
  wSliMR[7] = atan2(p10,p4);
  wSliMR[8] = rat(-20,3);
  wSliMR[9] = rat(-5,3);
  wSliMR[10] = log(p1);
  wSliMR[11] = li2re2(p3*p4*pow(p8,-1), - p10*p3*pow(p8,-1));
  wSliMR[12] = li2re2(p3*p4*pow(p8,-1),p10*p3*pow(p8,-1));
  wSliMR[13] = li2re2(inv2*p1, - inv2*p7*pow(p10,-1)*p1);
  wSliMR[14] = li2re2(inv2*p1,inv2*p7*pow(p10,-1)*p1);
  wSliMR[15] = li2re2( - x*pow(p1,-1)*p5, - x*p10*pow(p1,-1));
  wSliMR[16] = rat(22,3);
  wSliMR[17] = li2re2( - x*pow(p1,-1)*p5,x*p10*pow(p1,-1));
  wSliMR[18] = li2re2(1, - p7*pow(p10,-1));
  wSliMR[19] = rat(-34,3);
  wSliMR[20] = li2re2(1,p7*pow(p10,-1));
  wSliMR[21] = rat(-5,6);
  wSliMR[22] = rat(-41,6);
  wSliMR[23] = rat(5,3);
  wSliMR[24] = log(p3);
  wSliMR[25] = rat(34,3);
  wSliMR[26] = rat(-7,3);
  wSliMR[27] = log(p9);
  wSliMR[28] = log(p8);
  wSliMR[29] = rat(-5,12);
  wSliMR[30] = log(2);
  wSliMR[31] = li3re2(inv2, - inv2*p7*pow(p10,-1));
  wSliMR[32] = rat(32,3);
  wSliMR[33] = li3re2(inv2,inv2*p7*pow(p10,-1));
  wSliMR[34] = li3re2(pow(p1,-1)*p11*pow(p3,-1), - 2*x*p10*pow(p1,-1)*pow(p3,-1)*p9);
  wSliMR[35] = li3re2(pow(p1,-1)*p11*pow(p3,-1),2*x*p10*pow(p1,-1)*pow(p3,-1)*p9);
  wSliMR[36] = li3re2(inv2*p1, - inv2*p7*pow(p10,-1)*p1);
  wSliMR[37] = rat(-32,3);
  wSliMR[38] = li3re2(inv2*p1,inv2*p7*pow(p10,-1)*p1);
  wSliMR[39] = li3re2( - x*pow(p1,-1)*p5, - x*p10*pow(p1,-1));
  wSliMR[40] = li3re2( - x*pow(p1,-1)*p5,x*p10*pow(p1,-1));
  wSliMR[41] = li3re2(x*p4*pow(p8,-1), - x*p10*pow(p8,-1));
  wSliMR[42] = li3re2(x*p4*pow(p8,-1),x*p10*pow(p8,-1));
  wSliMR[43] = li3re2(2*x*y*pow(p1,-1)*pow(p3,-1)*p9, - 2*x*p10*pow(p1,-1)*pow(p3,-1)*p9);
  wSliMR[44] = li3re2(2*x*y*pow(p1,-1)*pow(p3,-1)*p9,2*x*p10*pow(p1,-1)*pow(p3,-1)*p9);
  wSliMR[45] = 1/(1 - 4*x*y + 2*pow(x,2) + 4*pow(x,2)*pow(y,2) - 4*pow(x,3)*y + pow(x,4));
  wSliMR[46] = li2im2(inv2, - inv2*p7*pow(p10,-1));
  wSliMR[47] = li2im2(inv2,inv2*p7*pow(p10,-1));
  wSliMR[48] = li2im2(pow(p1,-1)*p11*pow(p3,-1), - 2*x*p10*pow(p1,-1)*pow(p3,-1)*p9);
  wSliMR[49] = rat(-1,3);
  wSliMR[50] = li2im2(inv2*p1, - inv2*p7*pow(p10,-1)*p1);
  wSliMR[51] = li2im2(inv2*p1,inv2*p7*pow(p10,-1)*p1);
  wSliMR[52] = li2im2( - x*pow(p1,-1)*p5, - x*p10*pow(p1,-1));
  wSliMR[53] = li2im2( - x*pow(p1,-1)*p5,x*p10*pow(p1,-1));
  wSliMR[54] = 1/(2 - 8*x*y + 4*pow(x,2) + 8*pow(x,2)*pow(y,2) - 8*pow(x,3)*y + 2*pow(x,4));
  wSliMR[55] = li2im2(pow(p1,-1)*p11*pow(p3,-1),2*x*p10*pow(p1,-1)*pow(p3,-1)*p9);
  wSliMR[56] = 1/(3 - 12*x*y + 6*pow(x,2) + 12*pow(x,2)*pow(y,2) - 12*pow(x,3)*y + 3*pow(x,4));
  wSliMR[57] = 1/(6 - 24*x*y + 12*pow(x,2) + 24*pow(x,2)*pow(y,2) - 24*pow(x,3)*y + 6*pow(x,4));
  wSliMR[58] = PI*wSliMR[6];
  wSliMR[59] = wSliMR[7]*wSliMR[23];
  wSliMR[60] = wSliMR[2]*wSliMR[21];
  wSliMR[59] = wSliMR[60] + wSliMR[58] + wSliMR[59];
  wSliMR[59] = wSliMR[2]*wSliMR[59];
  wSliMR[60] = PI*wSliMR[4];
  wSliMR[61] = wSliMR[7]*wSliMR[21];
  wSliMR[61] = wSliMR[60] + wSliMR[61];
  wSliMR[61] = wSliMR[7]*wSliMR[61];
  wSliMR[62] = wSliMR[15] + wSliMR[17];
  wSliMR[63] = wSliMR[62]*wSliMR[16];
  wSliMR[64] = wSliMR[18] + wSliMR[20];
  wSliMR[65] = wSliMR[19]*wSliMR[64];
  wSliMR[66] = wSliMR[13] + wSliMR[14];
  wSliMR[67] = wSliMR[12] + wSliMR[11] + wSliMR[66];
  wSliMR[67] = wSliMR[4]*wSliMR[67];
  wSliMR[59] = wSliMR[59] + wSliMR[61] + wSliMR[67] + wSliMR[63] + wSliMR[65];
  wSliMR[59] = wSliMR[10]*wSliMR[59];
  wSliMR[61] = wSliMR[28]*wSliMR[23];
  wSliMR[65] = wSliMR[24]*wSliMR[6];
  wSliMR[67] = wSliMR[1]*wSliMR[4];
  wSliMR[68] = wSliMR[10]*wSliMR[22];
  wSliMR[61] = wSliMR[68] + wSliMR[67] + wSliMR[65] - wSliMR[30] + wSliMR[61];
  wSliMR[61] = wSliMR[5]*wSliMR[61];
  wSliMR[65] = wSliMR[7]*wSliMR[9];
  wSliMR[67] = PI*wSliMR[26];
  wSliMR[68] = wSliMR[2]*wSliMR[23];
  wSliMR[67] = wSliMR[68] + wSliMR[65] + wSliMR[67];
  wSliMR[67] = wSliMR[24]*wSliMR[67];
  wSliMR[68] = PI*wSliMR[8];
  wSliMR[69] = wSliMR[7]*wSliMR[4];
  wSliMR[70] = wSliMR[2]*wSliMR[6];
  wSliMR[68] = wSliMR[70] + wSliMR[68] + wSliMR[69];
  wSliMR[68] = wSliMR[1]*wSliMR[68];
  wSliMR[69] = wSliMR[7]*wSliMR[6];
  wSliMR[70] = wSliMR[2]*wSliMR[4];
  wSliMR[69] = wSliMR[70] + 16*PI + wSliMR[69];
  wSliMR[69] = wSliMR[10]*wSliMR[69];
  wSliMR[70] = PI*wSliMR[30];
  wSliMR[61] = wSliMR[61] + wSliMR[69] + wSliMR[68] + 2*wSliMR[70] + wSliMR[67];
  wSliMR[61] = wSliMR[5]*wSliMR[61];
  wSliMR[67] =  - wSliMR[53] - wSliMR[50] + wSliMR[46] - wSliMR[47] + wSliMR[51] + wSliMR[52];
  wSliMR[68] = wSliMR[2] - wSliMR[7];
  wSliMR[69] = wSliMR[68]*wSliMR[1];
  wSliMR[69] = wSliMR[67] + wSliMR[69];
  wSliMR[70] = wSliMR[56]*wSliMR[69];
  wSliMR[71] = 2*wSliMR[1];
  wSliMR[72] =  - wSliMR[71] + 2*wSliMR[10] + wSliMR[24] - wSliMR[28];
  wSliMR[72] = wSliMR[72]*wSliMR[5];
  wSliMR[73] = wSliMR[56]*wSliMR[72];
  wSliMR[74] = wSliMR[48] - wSliMR[55];
  wSliMR[75] =  - wSliMR[57]*wSliMR[74];
  wSliMR[70] = wSliMR[73] + wSliMR[70] + wSliMR[75];
  wSliMR[70] = wSliMR[70]*pow(x,2);
  wSliMR[69] =  - wSliMR[72] - wSliMR[69];
  wSliMR[69] = wSliMR[45]*wSliMR[69];
  wSliMR[72] = wSliMR[54]*wSliMR[74];
  wSliMR[69] = wSliMR[70] + wSliMR[72] + wSliMR[69];
  wSliMR[69] = x*y*wSliMR[69];
  wSliMR[68] = wSliMR[68]*wSliMR[71];
  wSliMR[67] = wSliMR[68] + wSliMR[55] + 2*wSliMR[67];
  wSliMR[67] = wSliMR[56]*wSliMR[67];
  wSliMR[68] = wSliMR[45]*wSliMR[48]*wSliMR[49];
  wSliMR[67] = wSliMR[69] + 2*wSliMR[73] + wSliMR[68] + wSliMR[67];
  wSliMR[67] = sqrOneMy2*x*wSliMR[67];
  wSliMR[68] = PI*wSliMR[28];
  wSliMR[69] = wSliMR[9]*wSliMR[68];
  wSliMR[70] = wSliMR[7]*wSliMR[28];
  wSliMR[71] = wSliMR[3]*wSliMR[70];
  wSliMR[72] = wSliMR[2]*wSliMR[28]*wSliMR[29];
  wSliMR[69] = wSliMR[72] + wSliMR[69] + wSliMR[71];
  wSliMR[69] = wSliMR[2]*wSliMR[69];
  wSliMR[71] = wSliMR[2]*wSliMR[3];
  wSliMR[60] = wSliMR[71] + wSliMR[60] + wSliMR[65];
  wSliMR[60] = wSliMR[2]*wSliMR[60];
  wSliMR[65] = wSliMR[7]*wSliMR[3];
  wSliMR[58] = wSliMR[58] + wSliMR[65];
  wSliMR[58] = wSliMR[7]*wSliMR[58];
  wSliMR[58] = wSliMR[58] + wSliMR[60];
  wSliMR[58] = wSliMR[1]*wSliMR[58];
  wSliMR[60] = wSliMR[25]*wSliMR[64];
  wSliMR[60] = wSliMR[63] + wSliMR[60];
  wSliMR[60] = wSliMR[24]*wSliMR[60];
  wSliMR[63] = wSliMR[23]*wSliMR[68];
  wSliMR[64] = wSliMR[29]*wSliMR[70];
  wSliMR[63] = wSliMR[63] + wSliMR[64];
  wSliMR[63] = wSliMR[7]*wSliMR[63];
  wSliMR[62] =  - 4*wSliMR[62];
  wSliMR[62] = wSliMR[27]*wSliMR[62];
  wSliMR[64] = wSliMR[33] + wSliMR[31];
  wSliMR[64] = wSliMR[32]*wSliMR[64];
  wSliMR[65] = wSliMR[36] + wSliMR[38] + wSliMR[40] + wSliMR[39];
  wSliMR[65] = wSliMR[37]*wSliMR[65];
  wSliMR[66] = wSliMR[30]*wSliMR[66];
  wSliMR[68] = wSliMR[35] + wSliMR[34];
  wSliMR[68] = wSliMR[21]*wSliMR[68];
  wSliMR[70] = wSliMR[42] + wSliMR[41];
  wSliMR[70] = wSliMR[6]*wSliMR[70];

  wSliMR[0] = wSliMR[43] + wSliMR[44] + wSliMR[58] +
    wSliMR[59] + wSliMR[60] + wSliMR[61] + wSliMR[62] +
    wSliMR[63] + wSliMR[64] + wSliMR[65] - 4*wSliMR[66] +
    wSliMR[67] + wSliMR[68] + wSliMR[69] + wSliMR[70];

  return wSliMR[0];
}
